package easy;

public class GameModel {
    private int curLvl;

    Maze maze;

    private static char[][][] levels = {
            {
                    {'*','*','#','#','#','#','#','*'},
                    {'#','#','#',' ',' ',' ','#','*'},
                    {'#',' ','B',' ','#',' ','#','#'},
                    {'#',' ','#',' ',' ','E',' ','#'},
                    {'#',' ',' ',' ',' ','#',' ','#'},
                    {'#','#',' ','#',' ',' ',' ','#'},
                    {'*','#','R',' ',' ','#','#','#'},
                    {'*','#','#','#','#','#','*','*'},
            },
            {
                     {'*','*','#','#','#','#','#','*'}, 
                     {'#','#','#',' ',' ',' ','#','*'}, 
                     {'#',' ','B',' ','#',' ','#','#'},
                     {'#',' ','#',' ',' ','E',' ','#'}, 
                     {'#',' ',' ',' ',' ','#',' ','#'}, 
                     {'#','#','B','#','E',' ',' ','#'},
                     {'*','#','R',' ',' ','#','#','#'}, 
                     {'*','#','#','#','#','#','*','*'}, 
            },
            {
                     {'*','*','#','#','#','#','#','*'}, 
                     {'#','#','#',' ',' ',' ','#','*'}, 
                     {'#',' ','B',' ','#',' ','#','#'},
                     {'#',' ','#',' ',' ','E',' ','#'}, 
                     {'#',' ','E',' ',' ','#',' ','#'},
                     {'#','#','B','#','E','B',' ','#'},
                     {'*','#','R',' ',' ','#','#','#'}, 
                     {'*','#','#','#','#','#','*','*'}, 
            }
    };

    public GameModel(){
        this.curLvl=0;
        maze = new Maze(levels[curLvl]);
    }


    public void nextLevel() {
        curLvl++;
        if(curLvl== levels.length){
            curLvl=0;
        }
        maze=new Maze(levels[curLvl]);
    }

    public Object getCurLvl() {
        return curLvl+1;
    }
}
