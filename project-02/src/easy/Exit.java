package easy;

public class Exit {
    private int exitRow;
    private int exitCol;

    public Exit(int exitRow, int exitCol){
        this.exitRow=exitRow;
        this.exitCol=exitCol;
    }

    public int getExitRow() {
        return exitRow;
    }
    public int getExitCol() {
        return exitCol;
    }
}
